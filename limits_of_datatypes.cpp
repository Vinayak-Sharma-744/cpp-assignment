//WAP to display the ranges of different datatypes. 

#include<iostream>
#include <limits.h>
using namespace std;

int main()
{
  cout << "char minimum value: " << CHAR_MIN << endl;
  cout << "char maximum value: " << CHAR_MAX << endl;
  cout<<endl;

  cout << "short  minimum value: " << SHRT_MIN << endl;
  cout << "short  maximum value: " << SHRT_MAX << endl;
  cout<<endl;

  cout << "int minimum value: " << INT_MIN << endl;
  cout << "int maximum value: " << INT_MAX << endl;
  cout<<endl;

  cout << "long  minimum value: " << LONG_MIN << endl;
  cout << "long  maximum value: " << LONG_MAX << endl;
  cout<<endl;

  cout << "long long  minimum value: " << LLONG_MIN << endl;
  cout << "long long  maximum value: " << LLONG_MAX << endl;
  cout<<endl;

  return 0;
}
 